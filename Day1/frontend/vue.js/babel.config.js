//=============================================================================
// babel.config.js
//
// BabelというJavaScriptトランスパイラの設定ファイルです
// Babelは最新のJavaScript（ES6以降）のコードを古いブラウザでも動作するようにトランスパイル（変換）します
//
// 役割
// - JavaScriptコードの互換性を確保
//   Babelを使用することで、最新のJavaScript構文をサポートしていないブラウザでもコードが動作するようにします
//   例えば、Promise や async/await などの機能を使用する際に、互換性を保つためのポリフィルを適用できます
// 
// - トランスパイルの対象範囲を定義
//   プリセットやプラグインを指定することで、どのJavaScript構文をトランスパイルするかを細かく制御します
// 
// - Vue.jsとの統合:
//   Vue.jsプロジェクトでは、@vue/cli が提供するプリセット（@vue/cli-plugin-babel/preset）を使用することで、
//   Vue特有の設定が自動的に適用されます
//=============================================================================

//Node.jsのモジュールシステムを使用して、Babelの設定をエクスポートします
module.exports = {

  //presets は、Babelが適用すべき事前定義された設定のセットを指定します
  presets: [
  
    //Vue CLIが提供するBabelプリセットです。主な役割は以下の通りです
    // - Vueプロジェクトに必要な最低限のトランスパイル設定を提供します
    // - 必要に応じて、ES6+の機能やポリフィルを追加します
    // - Vue特有のトランスパイルを適切に処理します
    '@vue/cli-plugin-babel/preset'
  ]
}
