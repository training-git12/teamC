//=============================================================================
// routes/adminRoutes.js
//
// 役割：このプログラムは、ECサイトの管理者向けAPIルートを提供するものです
//
//  管理者が以下の操作を行うためのエンドポイントを定義しています:
// - 管理APIの稼働確認: APIが正常に動作しているか確認するルート
// - 特定のユーザーの「いいね」リセット: 指定されたユーザーが「いいね」した全ての商品をリセットします
// - 特定の商品に関連する「いいね」リセット: 指定された商品の「いいね」情報を全てリセットします
// このルートは、管理者が「いいね」データを操作・管理する際に利用されます
//
// 目的
// - 管理者専用機能の提供: 通常のユーザーとは異なり、管理者がデータの操作を行えるようにします
// - データの整合性確保: 不正データやテストデータのクリーニング、または商品やユーザーの削除に伴う関連データの整理を容易にします
// - APIのモジュール化: 「管理者専用」のエンドポイントを他のAPIルートから分離することで、コードの可読性と管理性を向上させます
//=============================================================================

const express = require('express');
const router = express.Router();
const adminController = require('../controllers/adminController');

// ユーザーごとのいいねリセット
router.post('/reset-likes-by-user', adminController.resetLikesByUser);

// 商品ごとのいいねリセット
router.post('/reset-likes-by-product', adminController.resetLikesByProduct);

//router オブジェクトを他のファイルで使用できるようにモジュールとしてエクスポート
module.exports = router;
